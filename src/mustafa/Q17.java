package mustafa;

public class Q17 {
	private int num;
	private String str;
	
	public Q17(int num, String str){
		setNum(num);
		setStr(str);
		
	}
	
	public void setNum(int num){
		this.num=num;
	}
	
	public void setStr(String str){
		this.str=str;
	}
	
	public int getNum(){
		return this.num;
	}
	
	public String getStr(){
		return this.str;
	}

}
